function add(one, two){
  return one + two
}
function subtract(one, two){
  return one - two
}
function multiply(one, two){
  return one * two
}
function divide(one, two){
  return one / two
}
function inc(n){
  return n+=1
}
function dec(n){
  return n-=1
}
function makeInt(n){
  parseInt(n, 10) 
}
